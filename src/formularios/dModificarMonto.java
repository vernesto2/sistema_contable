    private void btnCargarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCargarActionPerformed
        try {
            if (txtMonto.getText().trim().equals("") || txtMonto.getText().contains("..")) {
                JOptionPane.showMessageDialog(this, "No ha ingresado un monto", "Â¡ALERTA!", JOptionPane.WARNING_MESSAGE);
            } if (txtFM.getText().trim().equals("") || txtFM.getText().contains("..") || txtFM.getText().trim().equals("0")) {
                JOptionPane.showMessageDialog(this, "No ha ingresado el FM valido", "Â¡ALERTA!", JOptionPane.WARNING_MESSAGE);
            } else {
                if (this.guardarFolioMayor()) {
                    montoIngresado = Double.parseDouble(txtMonto.getText());
                    montoFM = Integer.parseInt(txtFM.getText());
                    this.realizoAccion = true;
                    this.cerrar();
                }
                
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Ingrese un monto correcto", "Â¡ALERTA!", JOptionPane.WARNING_MESSAGE);
        }
    }//GEN-LAST:event_btnCargarActionPerformed

    public boolean guardarFolioMayor() {
        CicloContableFolio cicloFolioModel = new CicloContableFolio();
        cicloFolioModel.setId_ciclo_contable(this.sesion.configUsuario.getCicloContable().getId());
        cicloFolioModel.setId(this.cuenta.getId_ciclo_folio());
        cicloFolioModel.setId_cuenta(this.cuenta.getId());
        cicloFolioModel.setFolio_mayor(Integer.parseInt(this.txtFM.getText()));
        RespuestaGeneral rg;
        if (this.cuenta.getId_ciclo_folio() > 0) {
            rg = _cicloFolio.editar(cicloFolioModel);
        } else {
            rg = _cicloFolio.insertar(cicloFolioModel);
        }
        if (rg.esExitosa()) {
            this.id_ciclo_contable_folio = (int)rg.getDatos();
            return true;
        } else {
            JOptionPane.showMessageDialog(this, rg.getMensaje(), "Â¡ALERTA!", UtileriaVista.devolverCodigoMensaje(rg));
            return false;
        }
        
    }
    
